numero1 = int(input('Digite o primeiro numero: '))
numero2 = int(input('Digite o segundo numero: '))

if numero1 > numero2:
    print(f'\nO maior numero é {numero1}!')
elif numero1 < numero2:
    print(f'\nO maior numero é {numero2}!')
elif numero1 == numero2:
    print(f'\nOs dois numeros são iguais!')


